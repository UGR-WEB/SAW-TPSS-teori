var correctAnswers = 0;
var wrongAnswers = 0;
var usedQuestions = [];
var currentQuestion;
var answerIndex;

// İlk soruyu oluştur
CreateQuestion();

// Soruları tekrar etmeyecek şekilde oluşturma fonksiyonu
function CreateQuestion() {
    if (usedQuestions.length === questions.length) {
        usedQuestions = []; // Tüm sorular bittiyse listeyi sıfırla
    }

    var randomIndex;
    do {
        randomIndex = GetRandomIndex(questions.length);
    } while (usedQuestions.includes(randomIndex));

    usedQuestions.push(randomIndex);
    currentQuestion = questions[randomIndex];

    // Şıkları rastgele karıştır
    var shuffledOptions = ShuffleArray([...currentQuestion.secenekler]);
    answerIndex = shuffledOptions.indexOf(currentQuestion.secenekler[currentQuestion.cevap]);

    // Soruyu ve seçenekleri güncelle
    UpdateHTML("question_text", currentQuestion.soru);
    for (var i = 0; i < shuffledOptions.length; i++) {
        UpdateHTML("button_" + i, shuffledOptions[i]);
    }
}

// Cevabı kontrol et ve yeni soru oluştur
function CheckAndCreateNewQuestion(answer) {
    if (answer == answerIndex) {
        correctAnswers++;
        swal({
            title: "Doğru cevap!",
            text: "Tebrikler! Doğru cevabınız: <strong>" + currentQuestion.secenekler[answer] + "</strong>",
            type: "success",
            confirmButtonColor: "#28a745",
            confirmButtonText: "Sıradaki Soru",
            customClass: "sweetAlertModal",
            html: true
        }, function () {
            CreateQuestion();
            UpdateHTML("correctAnswers", correctAnswers);
        });
    } else {
        wrongAnswers++;
        swal({
            title: "Yanlış cevap :(",
            text: "Doğru cevap: <strong>" + currentQuestion.secenekler[currentQuestion.cevap] + "</strong>",
            type: "error",
            confirmButtonColor: "#dc3545",
            confirmButtonText: "Sıradaki Soru",
            customClass: "sweetAlertModal",
            html: true
        }, function () {
            CreateQuestion();
            UpdateHTML("wrongAnswers", wrongAnswers);
        });
    }
}

// Rastgele indeks oluşturur
function GetRandomIndex(end) {
    return Math.floor(Math.random() * end);
}

// Şıkları rastgele karıştırma fonksiyonu
function ShuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        let j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

// HTML güncelleme fonksiyonu
function UpdateHTML(id, text) {
    var element = document.getElementById(id);
    if (element) {
        element.innerHTML = text;
    }
}
